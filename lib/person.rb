class Person 
  
  def person_name(name)
    @name = name 
  end
  
  def name 
    @name
  end
  
  def name=(new_name) #setter method
    @name = new_name
  end
  
  def person_job(job)
    @job = job 
  end
  
  def job 
    @job
  end
  
  def job=(new_job) #setter method
    @job = new_job
  end
  
end